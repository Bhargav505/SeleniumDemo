package classDemo;

public class StringDemo {

	public static void main(String[] args) {
		
		//syntax1
		
		String S1="sachin";
	    System.out.println("s1 is"+S1);
	    String S2="SACHIN";
	    System.out.println("s2 is"+S2);
	    System.out.println(S1.equals(S2)); // comparing the two strings
	    System.out.println(S1.equalsIgnoreCase(S2)); // Equals ignorecase
	    System.out.println(S1==S2);  // using equals operator
	    
	    //syntax 2
	    String s3=new String("Alluri");
	    System.out.println("s2 is"+s3);
		
		
		
		
		
		

	}

}
